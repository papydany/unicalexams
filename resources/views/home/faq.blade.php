                                        <div class="panel-body">
                                      Probation simply means, a student is asked to repeat the year such a student just finished. The reason for such repeat could be because of one of the conditions below or both.<br/>
(i). A student of any year must have failed within (10 to 15) credit units in first and second semesters, or from first semester/second semester only after the completion of a particular academic session.<br/>
(ii). A student’s GPA or CGPA is less than 1.5 after completing a session.<br/> 
<b>Note 1:</b> Condition (i) overrides condition (ii). Meaning that, regardless of the GPA or CGPA, if a student fails within (10-15) credit units in any year, he/she will be asked to probate/or repeat the year.<br/>
<b>Note 2:</b> Probating or Repeating the year, the student is expected to register and seat for only his/her previously failed or dropped courses. He is not expected to take any newly introduced courses of the year he/she is repeating or any new level course.<br/>
<b>Note 3: </b>A student may be asked to withdraw or change programme, if after probation. He/she fails to attain a GPA/CGPA of 1.5

